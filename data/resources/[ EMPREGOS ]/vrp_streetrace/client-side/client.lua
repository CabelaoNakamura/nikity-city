function cnVRP.defuseRace()
	inRace = false
	timeSeconds = 0

	for k,v in pairs(blipRace) do
		if DoesBlipExist(blipRace[k]) then
			RemoveBlip(blipRace[k])
			blipRace[k] = nil
		end
	end
end